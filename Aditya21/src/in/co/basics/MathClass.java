package in.co.basics;

public class MathClass {
	public static void main(String[] args) {

		System.out.println("Mathematics Functions");
		System.out.println("Max2,5=" + Math.max(2, 5));
		System.out.println("Min2,5=" + Math.min(2, 5));
		System.out.println("Absolute 3.7=" + Math.abs(3.7));
		System.out.println("Exp 10=" + Math.exp(10));// not work other value pi
		System.out.println("Random Number=" + Math.random());
		System.out.println("Square Root=" + Math.sqrt(9));

	}
}
