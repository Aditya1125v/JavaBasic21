package InterfaceClass;

public class Rupali {
	private String colour;
	private int boarderwidth;
	public Rupali(String colour,int boarderwidth) { 
		this.colour=colour;
		this.boarderwidth=boarderwidth;
	}
	public String getColour() {
		return colour;
	}
	public int getBoarderwidth() {
		return boarderwidth;
	}
	
	

}
